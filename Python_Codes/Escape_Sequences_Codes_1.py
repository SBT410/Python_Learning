print("Hi , this is my first code in python")

# Alternative way to include double quotes in the string
print("Hi , this is \"my first code\" in python")

# Alternative way to include single quotes in the string
print("Hi , this is \'my first code\' in python")

# Using escape sequences to include backslashes in the string
print("Path: C:\\Users\\Name\\Desktop\\")

# Using escape sequences to include newlines in the string
print("Message1")
print("Message2")

print("Message1\n")
print("Message2")

print("Message1\n\nMessage2")

# Using escape sequences to include tabs in the string
print("Message1\tMessage2")

